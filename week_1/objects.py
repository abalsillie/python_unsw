# object types:
# - integer
# - floats
# - string e.g. 'Hello', 'we34t&2*'
# - boolean True or False
# - list ordered collection of objects
# - set unordered collection of unique objects
# - none

print(type(False)) # prints the object type
print(isinstance(1, int)) # prints true or false
print(isinstance(3.14, float))
print(isinstance('Hello', str))

print("Hello".upper()) # prints hello in uppercase
print("Hello".lower()) # prints hello in lowercase
